# Demonstrates the use of control structures in Python

# if-else statement
age = 20
if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# for loop
print("For loop example:")
for i in range(5):
    print(f"i = {i}")

# while loop
print("While loop example:")
count = 0
while count < 5:
    print(f"count = {count}")
    count += 1
