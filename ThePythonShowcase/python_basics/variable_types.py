# Demonstrates the use of different data types in Python

# Integer
my_int = 10
print(f"Integer: {my_int}")

# Float
my_float = 20.5
print(f"Float: {my_float}")

# String
my_string = "Hello, Python!"
print(f"String: {my_string}")

# List
my_list = [1, 2, 3, "Python", "List"]
print(f"List: {my_list}")

# Tuple
my_tuple = (1, 2, 3, "Python", "Tuple")
print(f"Tuple: {my_tuple}")

# Dictionary
my_dict = {"name": "Alice", "age": 30}
print(f"Dictionary: {my_dict}")

# Set
my_set = {1, 2, 3, "Python", "Set"}
print(f"Set: {my_set}")
